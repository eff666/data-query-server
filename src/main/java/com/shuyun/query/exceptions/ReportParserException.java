package com.shuyun.query.exceptions;

public class ReportParserException extends Exception {

    public ReportParserException() {
        super();
    }

    public ReportParserException(String message) {
        super(message);
    }

    public ReportParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public ReportParserException(Throwable cause) {
        super(cause);
    }

}
